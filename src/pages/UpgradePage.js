export const renderUpgradePage = () => {
  const upgradePage = document.querySelector('.js-upgrade-page');
  const questions = document.querySelectorAll('.question-section');
  questions.forEach(question => {
    question.addEventListener('click', e => {
      const iconEl = question.querySelector('i');
      iconEl.classList.toggle('rotate-[180deg]');
      const answer = question.querySelector('.answer');
      answer.classList.toggle('hidden');
      answer.classList.toggle('block');
    });
  });
};
window.addEventListener('scroll', () => {
  const header = document.querySelector('.js-header');

  if (window.scrollY > 0) {
    header.classList.add('border-b-gray-500');
  } else {
    header.classList.remove('border-b-gray-500');
  }
});
